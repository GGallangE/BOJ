import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.StringTokenizer;

public class 백준11049번 {
	public static void main(String[] args) throws NumberFormatException, IOException {
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		StringTokenizer st;
		int N = Integer.parseInt(br.readLine());
		int[][] dp = new int[N][N]; // 연산 횟수 배열
		int[][] data = new int[N][2]; // 입력값을 받아주는 배열
		for(int i=0; i<N; i++) {
			st = new StringTokenizer(br.readLine());
			data[i][0] = Integer.parseInt(st.nextToken());  // 입력값 : 행
			data[i][1] = Integer.parseInt(st.nextToken());  // 입력값 : 열
		}
		for(int k=1; k<N; k++) {
			for(int i=0; i+k<N; i++) {
				dp[i][i+k] = Integer.MAX_VALUE; // 최소값을 찾기위해 일부러 최대값을 넣어줌.
				for(int j=i; j<i+k; j++)
					dp[i][i+k] = Math.min(dp[i][i+k], dp[i][j]+dp[j+1][i+k] + data[i][0]*data[j][1]*data[i+k][1]); // J는 괄호를 나누는 기준점.
			}
		}
		
		
		System.out.println(dp[0][N-1]);
	}
}